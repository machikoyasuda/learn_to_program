
def my_method(miles_per_day, days_per_week)
	puts "You bike #{(miles_per_day * days_per_week)} miles a week."
end

my_method(3, 5)

my_miles = 3
my_days = 5
my_method(my_miles, my_days)

my_method(my_miles * 2, my_days)

my_method(my_miles + 5, my_days - 1)

my_method(my_miles + 234, my_days)

my_method(12,4)

my_method(12*3, 5)